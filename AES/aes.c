// Galois Field 8 곱셈
uint8_t gf8_mul(uint8_t a, uint8_t b)
{
	uint8_t r = 0;
	
	while (b > 0) {
		if (b & 1) {
			r = r ^ a;
		}
		b = b >> 1;
		a = ((a<<1) ^ ((a>>7) & 1 ? 0x1B : 0));
	}
	return r;
}

// AddRoundKey
static void AddRoundKey(uint8_t *state, const uint32_t *roundKey, uint8_t index)
{
  uint8_t *p; // roundkey 읽기용

  uint8_t i;
  for (i = 0; i < Nk; i++) {
    p = (uint8_t *)(roundKey + i + 4 * index); // index를 넣어 읽어야 하는 위치를 조정
    // AddRoundKey 연산
    state[i * 4] = state[i * 4] ^ p[0];
    state[i * 4 + 1] = state[i * 4 + 1] ^ p[1];
    state[i * 4 + 2] = state[i * 4 + 2] ^ p[2];
    state[i * 4 + 3] = state[i * 4 + 3] ^ p[3];
  }
}

// SubBytes
static void SubBytes(uint8_t *state, int mode)
{
  uint8_t count;
  for (count = 0; count < 16; count++) {
    if (mode == ENCRYPT) { // mode가 ENCRYPT인 경우
      state[count] = sbox[state[count]];
    } else { // mode가 DECRYPT인 경우
      state[count] = isbox[state[count]];
    }
  }
}

// ShiftRows
static void ShiftRows(uint8_t *state, int mode) {
  uint8_t tmp[BLOCKLEN]; // 임시 저장 변수
  uint8_t corr = 0; // ENCRYPT / DECRYPT 에 따라 인덱스 보정값
  uint8_t index;

  memcpy(tmp, state, BLOCKLEN); // state 통째로 복사

  // Row 2 << / DECRYPT는 Row 4
  if (mode == DECRYPT) { 
    corr = 2;
  }
  index = 1 + corr;
  state[index] = tmp[index + 4];
  state[index + 4] = tmp[index + 8];
  state[index + 8] = tmp[index + 12];
  state[index + 12] = tmp[index];

  // Row 3 스왑 / DECRYPT 공통
  index = 2;
  state[index] = tmp[index + 8];
  state[index + 4] = tmp[index + 12];
  state[index + 8] = tmp[index];
  state[index + 12] = tmp[index + 4];

  // Row 4 >> / DECRYPT는 Row 2
  index = 3 - corr;
  state[index] = tmp[index + 12];
  state[index + 4] = tmp[index];
  state[index + 8] = tmp[index + 4];
  state[index + 12] = tmp[index + 8];
}

// MixColumns
static void MixColumns(uint8_t *state, int mode) { 
  uint8_t tmp[BLOCKLEN]; // 임시 저장 변수
  uint8_t i, j; // for문 변수

  memcpy(tmp, state, BLOCKLEN); // state 복사

  for (i = 0; i < Nb; i++) { // 행
    for (j = 0; j < Nk; j++) { // 열
      // 행렬의 각 원소 계산
      uint8_t sub1;
      uint8_t sub2;
      uint8_t sub3;
      uint8_t sub4;
      
      if (mode == ENCRYPT) { // mode가 ENCRYPT 일 경우
        sub1 = gf8_mul(M[i * 4 + 0], tmp[j * 4 + 0]); // 비트 오더에 의해 이상하게 저장되어서
        sub2 = gf8_mul(M[i * 4 + 1], tmp[j * 4 + 1]); // 일반적인 행렬 순서와 다름.
        sub3 = gf8_mul(M[i * 4 + 2], tmp[j * 4 + 2]);
        sub4 = gf8_mul(M[i * 4 + 3], tmp[j * 4 + 3]);
      } else { // mode가 DECRYPT 일 경우
        sub1 = gf8_mul(IM[i * 4 + 0], tmp[j * 4 + 0]);
        sub2 = gf8_mul(IM[i * 4 + 1], tmp[j * 4 + 1]);
        sub3 = gf8_mul(IM[i * 4 + 2], tmp[j * 4 + 2]);
        sub4 = gf8_mul(IM[i * 4 + 3], tmp[j * 4 + 3]);
      }
      
      state[i + j * 4] = sub1 ^ sub2 ^ sub3 ^ sub4; // galois 계산한걸 결합
    }
  }
  
}

