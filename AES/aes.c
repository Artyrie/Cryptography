/*
 * Generate an AES key schedule
 */
void KeyExpansion(const uint8_t *key, uint32_t *roundKey)
{
  uint8_t count = 0; // 라운드를 셀 변수

  // 초기 워드 설정. 2개씩 있는걸 결합
  while (count < 4) {
    uint32_t sub1 = key[4 * count + 0];
    uint32_t sub2 = (key[4 * count + 1] << 8);
    uint32_t sub3 = (key[4 * count + 2] << 16);
    uint32_t sub4 = (key[4 * count + 3] << 24);
    roundKey[count] = sub1 ^ sub2 ^ sub3 ^ sub4;
    count++;
  }
  
  // KeyExpansion 진행
  for (count = 1; count < sizeof(Rcon); count++) {
    uint8_t index = count * 4;
    uint32_t lRotWord; // Left Rotation Word 값 저장

    // 비트 오더에 의해 형태가 이상하나 1byte shift 연산
    uint8_t tmp = (roundKey[index - 1]);
    lRotWord = (roundKey[index -1] >> 8) ^ (tmp << 24);
    
    // lRotWord 값을 두개씩 sbox로 변환 후 결합
    uint8_t sub1 = sbox[(uint8_t)(lRotWord >> 24)];
    uint8_t sub2 = sbox[(uint8_t)(lRotWord >> 16)];
    uint8_t sub3 = sbox[(uint8_t)(lRotWord >> 8)];
    uint8_t sub4 = sbox[(uint8_t)(lRotWord)];
    lRotWord = (sub1 << 24) ^ (sub2 << 16) ^ (sub3 << 8) ^ sub4;

    // KeyExpansion 연산
    roundKey[index] = roundKey[index - 4] ^ (lRotWord ^ Rcon[count]);
    roundKey[index + 1] = roundKey[index - 3] ^ roundKey[index];
    roundKey[index + 2] = roundKey[index - 2] ^ roundKey[index + 1];
    roundKey[index + 3] = roundKey[index - 1] ^ roundKey[index + 2];
  }
}

/*
 * AES cipher function
 * If mode is nonzero, then do encryption, otherwise do decryption.
 */
void Cipher(uint8_t *state, const uint32_t *roundKey, int mode)
{
  uint8_t index = 0; // ENCRYPT 일 경우 roundKey 시작 인덱스
  uint8_t corr = 1; // ENCRYPT 인덱스 증감량

  uint8_t i; // for 함수용

  if (mode == DECRYPT) { // DECRYPT 일 경우 roundKey 시작 인덱스, 증감량
    index = 10;
    corr = -1;
  }

  // 초기 AddRoundKey
  AddRoundKey(state, roundKey, index);
  index += corr;

  // main
  for(i = 0; i < Nr; i++) {
    SubBytes(state, mode);
    ShiftRows(state, mode);
    if (mode == ENCRYPT) { // ENCRYPT의 경우
      if (i != 9) { // 마지막 라운드 제외
        MixColumns(state, mode);
      }
      AddRoundKey(state, roundKey, index);
    } else { // DECRYPT의 경우
      AddRoundKey(state, roundKey, index);
      if (i != 9) { // 마지막 라운드 제외
        MixColumns(state, mode);
      }
    }
    
    index += corr;
  }
}